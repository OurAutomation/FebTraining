package org.methods;

public class CalculatorFunctionality {
    public int add(int i, int j) {
        return i + j;
    }

    public int subtraction(int i, int j) {
        return i - j;
    }

    public int multiply(int i, int j) {
        return i * j;
    }

    public int square(int i) {
        return i * i;
    }

}
