package org.OOPS.inheritance;

public class CalculatorFunctionality extends MultiLevelInheritance {
    private String str;
    public int add(int i, int j) {
        return i + j;
    }

    public int subtraction(int i, int j) {
        return i - j;
    }

    public int multiply(int i, int j) {
        return i * j;
    }

}
