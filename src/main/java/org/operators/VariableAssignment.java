package org.operators;

public class VariableAssignment {
    public static void main(String[] args) {
        int firstVariable = 12, secondVariable = 5;
        // = is assignment operator
        System.out.println(firstVariable + secondVariable);
        System.out.println(firstVariable - secondVariable);
        System.out.println(firstVariable * secondVariable);
        System.out.println(firstVariable / secondVariable);
        System.out.println(firstVariable % secondVariable);
    }
}
